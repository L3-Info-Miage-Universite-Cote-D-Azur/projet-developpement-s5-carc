package logic.tile.chunk;

/**
 * The type of chunk.
 */
public enum ChunkType {
    /**
     * The chunk is a road.
     */
    ROAD,

    /**
     * The chunk is a road end.
     */
    ROAD_END,

    /**
     * The chunk is a town.
     */
    TOWN,

    /**
     * The chunk is an abbey.
     */
    ABBEY,

    /**
     * The chunk is a field.
     */
    FIELD
}
