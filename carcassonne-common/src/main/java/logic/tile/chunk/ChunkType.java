package logic.tile.chunk;

/**
 * The type of chunk.
 */
public enum ChunkType {
    /**
     * The chunk contains a road.
     */
    ROAD,

    /**
     * The chunk contains a town.
     */
    TOWN,

    /**
     * The chunk contains a abbey.
     */
    ABBEY,

    /**
     * The chunk contains a field.
     */
    FIELD,

    /**
     * The chunk contains a road end.
     */
    ROAD_END
}
