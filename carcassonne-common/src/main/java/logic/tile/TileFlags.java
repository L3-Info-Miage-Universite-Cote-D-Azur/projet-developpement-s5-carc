package logic.tile;

/**
 * Enum for the different flags of tiles.
 */
public enum TileFlags {
    /**
     * The tile is a start tile.
     */
    STARTING,

    /**
     * The tile is a shield tile.
     */
    SHIELD,

    /**
     * The tile is a princess tile.
     */
    PRINCESS,

    /**
     * The tile is a dragon tile.
     */
    DRAGON,

    /**
     * The tile has a portal.
     */
    PORTAL,

    /**
     * The tile is a volcano.
     */
    VOLCANO,
}
